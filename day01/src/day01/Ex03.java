package day01;

public class Ex03 {

	public static void main(String[] args) {
		// 주석은 소스에 대한 팁을 다는 것, 컴파일시에 제외된다
		int a = 0;
		byte b = 0;
		System.out.println(a++); // 0을 표시한 뒤에 1을 더한다
		System.out.println(a); // 1이 나올 것임
		System.out.println(++b); // 1을 더한 값을 뿌려준다
		short s = 0;
		long l = 0;
		System.out.println(s);
		System.out.println(l);
		
	}

}
