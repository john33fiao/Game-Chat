package day01;

public class Ex04 {

	public static void main(String[] args) {
		// 형변환
		double d = 3.00001;
		System.out.println(d);
		float f = 3.40f;
		System.out.println(f);
		String s = "자바";
		System.out.println(s);
		char c = 'a';
		System.out.println(c);
		// 자동 형변환
		System.out.println((char)97);
		
	}

}
